package com.ct;

public class Even_number {

	public static void main(String[] args) {
		int result = 0;
		for (int i = 0; i <=100; i = i+2) {
			System.out.println(i+". This is even number.");
			result = result +i;
			System.out.println(" current value is: "+i);
			
			System.out.println("  and the result is: " +result);
			System.out.println(" this the even number.");
		}

	}

}
